package Java_Education.day04_incremets_decrements;

public class Day03_DataCasting1 {
    public static void main(String[] args) {

        int number1= 20;
        int number2= 6;
        System.out.println(number1/number2); // 3
        // if the dividend and divisor are both whole numbers, Java only takes the whole number part

        double number3=6;
        System.out.println(number1/number3); // 3,33333333333333335 int / double Java takes action accourding to the larger one

    }
}
